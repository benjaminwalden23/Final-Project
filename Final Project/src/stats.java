public class stats
		{
		String character;
		int attack;
		int speed;
		public stats(String c, int a, int s)
			{
				character = c;
				attack = a;
				speed = s;
				
				
	}
		public String getCharacter()
			{
				return character;
			}
		public void setCharacter(String character)
			{
				this.character = character;
			}
		public int getAttack()
			{
				return attack;
			}
		public void setAttack(int attack)
			{
				this.attack = attack;
			}
		public int getSpeed()
			{
				return speed;
			}
		public void setSpeed(int speed)
			{
				this.speed = speed;
			}

	}
